#[tokio::test]
async fn post_without_content_type_is_mlflow_error() {
    let server = TestServer::start("no_ct").await;
    let body = json!({"query": "query Q { test(inputString: \"x\") { output } }"});
    // POST without content-type → `_validate_content_type` rejects *before*
    // GraphQL execution, so this is a normal MlflowException JSON error.
    let res = send_raw(
        &server,
        Method::POST,
        Bytes::from(serde_json::to_vec(&body).unwrap()),
        false,
    )
    .await;
    // Not an in-band 200 GraphQL body — it's a proper error response.
    assert_ne!(res.status, StatusCode::OK, "{}", res.text());
    let body = res.json();
    assert_eq!(body["error_code"], "INVALID_PARAMETER_VALUE");
    assert!(body["message"].as_str().unwrap().contains("Content-Type"));
}

