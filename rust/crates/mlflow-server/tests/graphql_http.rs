const WS: &str = "default";

// The fixture's pre-seeded entities (see `tests/fixtures/tracking.db`).
const FIXTURE_EXP_ID: &str = "1";
const FIXTURE_RUN_ID: &str = "f6f9b188dd2845c0ae79e10bce95d692";

fn fixture_path() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("tests")
        .join("fixtures")
        .join("tracking.db")
}

struct TempDb {
    path: PathBuf,
}

impl TempDb {
    fn new(tag: &str) -> Self {
        static COUNTER: std::sync::atomic::AtomicU64 = std::sync::atomic::AtomicU64::new(0);
        let n = COUNTER.fetch_add(1, std::sync::atomic::Ordering::Relaxed);
        let path = std::env::temp_dir().join(format!(
            "mlflow_rust_server_graphql_{}_{}_{}.db",
            tag,
            std::process::id(),
            n
        ));
        let _ = std::fs::remove_file(&path);
        std::fs::copy(fixture_path(), &path).expect("copy fixture");
        TempDb { path }
    }

    fn uri(&self) -> String {
        format!("sqlite:///{}", self.path.display())
    }
}

impl Drop for TempDb {
    fn drop(&mut self) {
        let _ = std::fs::remove_file(&self.path);
    }
}

struct TestServer {
    base: String,
    shutdown: Option<tokio::sync::oneshot::Sender<()>>,
    handle: Option<tokio::task::JoinHandle<()>>,
    _db: TempDb,
    registry: RegistryStore,
}

impl TestServer {
    async fn start(tag: &str) -> Self {
        let db_file = TempDb::new(tag);

        let db = Db::connect(&db_file.uri(), PoolConfig::default())
            .await
            .expect("connect temp fixture");
        let tracking = TrackingStore::new(db.clone(), "file:///unused".to_string());
        let registry = RegistryStore::new(db);

        let config = ServerConfig {
            host: "127.0.0.1".to_string(),
            port: 0,
            static_prefix: None,
            backend_store_uri: None,
            default_artifact_root: None,
            serve_artifacts: true,
            artifacts_destination: None,
            allowed_hosts: None,
            cors_allowed_origins: None,
            x_frame_options: "SAMEORIGIN".to_string(),
        };
        let recorder = PrometheusBuilder::new().build_recorder().handle();
        let app_state =
            AppState::with_registry(tracking.clone(), registry.clone(), true, None, None);
        let app = build_app_with_recorder(&config, recorder, Some(app_state));

        let listener = TcpListener::bind(("127.0.0.1", 0)).await.expect("bind");
        let addr = listener.local_addr().expect("addr");
        let (shutdown_tx, shutdown_rx) = tokio::sync::oneshot::channel::<()>();
        let handle = tokio::spawn(async move {
            axum::serve(listener, app)
                .with_graceful_shutdown(async {
                    let _ = shutdown_rx.await;
                })
                .await
                .expect("server error");
        });

        TestServer {
            base: format!("http://{addr}"),
            shutdown: Some(shutdown_tx),
            handle: Some(handle),
            _db: db_file,
            registry,
        }
    }
}

impl Drop for TestServer {
    fn drop(&mut self) {
        if let Some(tx) = self.shutdown.take() {
            let _ = tx.send(());
        }
        if let Some(h) = self.handle.take() {
            h.abort();
        }
    }
}

struct HttpResponse {
    status: StatusCode,
    content_type: Option<String>,
    body: Vec<u8>,
}

impl HttpResponse {
    fn text(&self) -> String {
        String::from_utf8_lossy(&self.body).into_owned()
    }
    fn json(&self) -> Value {
        serde_json::from_slice(&self.body)
            .unwrap_or_else(|e| panic!("body is not JSON: {e}: {}", self.text()))
    }
}

/// Send a GraphQL request. `method` is GET or POST; both carry the JSON body
/// (graphene reads `get_json(force=True)` regardless of verb). `with_ct`
/// controls whether the `content-type: application/json` header is sent.
async fn send_raw(server: &TestServer, method: Method, body: Bytes, with_ct: bool) -> HttpResponse {
    let client = Client::builder(TokioExecutor::new()).build_http();
    let url = format!("{}/graphql", server.base);
    let build = || {
        let mut b = Request::builder().method(method.clone()).uri(&url);
        if with_ct {
            b = b.header("content-type", "application/json");
        }
        b.body(Full::<Bytes>::new(body.clone())).unwrap()
    };

    let mut last = None;
    for _ in 0..50 {
        match client.request(build()).await {
            Ok(res) => {
                let status = res.status();
                let content_type = res
                    .headers()
                    .get("content-type")
                    .and_then(|v| v.to_str().ok())
                    .map(str::to_string);
                let bytes = res.into_body().collect().await.unwrap().to_bytes();
                return HttpResponse {
                    status,
                    content_type,
                    body: bytes.to_vec(),
                };
            }
            Err(e) => {
                last = Some(e);
                tokio::time::sleep(std::time::Duration::from_millis(20)).await;
            }
        }
    }
    panic!("failed to connect: {last:?}");
}

/// POST a `{query, variables}` GraphQL request with the JSON content-type.
async fn gql(server: &TestServer, query: &str, variables: Value) -> HttpResponse {
    let body = json!({"query": query, "variables": variables});
    send_raw(
        server,
        Method::POST,
        Bytes::from(serde_json::to_vec(&body).unwrap()),
        true,
    )
    .await
}

fn assert_ok(res: &HttpResponse) {
    assert_eq!(res.status, StatusCode::OK, "{}", res.text());
    assert_eq!(
        res.content_type.as_deref(),
        Some("application/json"),
        "{}",
        res.text()
    );
    // Flask `jsonify` appends a trailing newline.
    assert!(res.body.ends_with(b"\n"), "body must end with newline");
}

