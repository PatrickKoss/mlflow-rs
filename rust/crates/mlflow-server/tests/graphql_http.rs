//! HTTP integration + schema-parity tests for the `/graphql` endpoint (plan
//! T6.1/T6.2, §3.12). Boots the axum app on a real ephemeral socket (same
//! harness as `registry_http.rs`) with a registry-enabled `AppState`, then
//! drives every implemented GraphQL operation over both GET and POST, plus the
//! no-batching / query-safety guard and the graphene-faithful error shapes.
//!
//! Coverage:
//! * each Query operation (`mlflowGetExperiment`, `mlflowGetRun`,
//!   `mlflowGetMetricHistoryBulkInterval`, `mlflowListArtifacts`,
//!   `mlflowSearchModelVersions`) and each Mutation (`mlflowSearchRuns`,
//!   `mlflowSearchDatasets`, plus the `test`/`testMutation` echo fields);
//! * GET and POST both parse the JSON body (no query-string parsing);
//! * `LongString` scalars serialize as *quoted* integers; `Float` metric
//!   values keep Python float repr; `apiError` is always `null`;
//! * the in-band error shape (`{"data": ..., "errors": ["<message>"]}` — bare
//!   strings, failed root field `null`);
//! * the no-batching guard (root-fields / aliases / depth / total-selections)
//!   and the batched-array / missing-query rejections;
//! * content-type validation on POST (MlflowException JSON, not in-band);
//! * T6.2: the embedded SDL is byte-identical to the on-disk
//!   `autogenerated_schema.gql`, and every implemented root field is present in
//!   that schema.

